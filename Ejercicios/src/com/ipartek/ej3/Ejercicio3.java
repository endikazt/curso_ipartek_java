public class Ejercicio3 {

	public static void main(String[] args) {

		int numeroA = 8;
		int numeroB = 3;
		int numeroC = -5;
		
		System.out.println("A: " + (numeroA+numeroB+numeroC));
		
		System.out.println("B: " + (2 * numeroB + 3 * (numeroA - numeroC)));
		
		System.out.println("C: " + (numeroA/numeroB));
		
		System.out.println("D: " + (numeroA % numeroB));
		
		System.out.println("E: " + (numeroA / numeroB));
		
		System.out.println("F: " + (numeroA % numeroB));
		
		System.out.println("G: " + (numeroA * numeroB / numeroC));
		
		System.out.println("H: " + (numeroA * (numeroB / numeroC)));
		
		System.out.println("I: " + ((numeroA*numeroC) % numeroB));
		
		System.out.println("J: " + (numeroA * (numeroC % numeroB)));
		
		System.out.println("K: " + ((3 * numeroA - 2 * numeroB) % (2 * numeroA - numeroC)));
		
		System.out.println("L: " + (2 * (numeroA / 5 + (4 - numeroB * 3))));
		
		System.out.println("M: " + ((numeroA - 3 * numeroB) % (numeroC + 2 * numeroA) / (numeroA - numeroC)));
		
		System.out.println("N: " + ((numeroA - numeroB - numeroC * 2)));
		
		//EJERCICIOS FLOAT
		
		double numeroX = 88;
		
		double numeroY = 3.5;
	
		double numeroZ = -5.2;
		
		System.out.println("A: " + (numeroX+numeroY+numeroZ));
		
		System.out.println("B: " + (2 * numeroY + 3 * (numeroX - numeroZ)));
		
		System.out.println("C: " + (numeroX/numeroY));
		
		System.out.println("D: " + (numeroX % numeroY));
		
		

	}

}
